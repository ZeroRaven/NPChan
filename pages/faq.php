		<h3>Boards</h3>
		<p>Consider board as a (facebook) group where anyone can add new posts according to the theme/category of the board. For example, a board "<a href="/g/">Technology</a>" consists of everything related to technology (hardware and software) and a board "<a href="/lit/">Literature</a>" consists of everything related to literature.</p>
		<p>Each board have their own unique link. These link are found on address bar as: <code>http://npchan.com/{LINK}/</code>.
			For example, "Technology" board's link is <b>/g/</b> and "Literature" board's link is <b>/lit/</b>.</p>
		<p>You can create a link to any board using following syntax on any <a href="#thread">thread</a> or <a href="#reply">thread reply</a>: <code>&gt;&gt;&gt;/{link}/</code>. Example: <b><a href="/lit/">&gt;&gt;&gt;/lit/</a></b>. (<a href="#formatting">Learn more</a>)</p>
	</div>



	<!-- Threads -->
	<div class="card-footer faq" id="thread">
		<h3>Threads</h3>
		<p>Thread is a post created within a board. Every thread starts a new topic to discuss on. A thread can be started with an image or a text or both.</p>
		<p>Creating a thread is as easy as posting "feeling sick" on your fav. social media. On the top of the boards page, there is a form where you can type what you want to say or upload an image.</p>
		<p>Either text or an image is required to create a new thread. Subject for a thread is optional.</p>
		<p>Please make thread sensibly. Avoid duplicate thread and respect the board rules.</p>
		<p>Every thread can have many replies within like comment on posts at your fav. social media.</p>
	</div>



	<!-- Reply -->
	<div class="card-footer faq" id="reply">
		<h3>Reply</h3>
		<p>Reply or comment is what you respond on a thread. There are no "like", "react" or "upvote" thing on NPChan. Thread is the only way to respond. Reply, like threads, can be created either with image or a text or both.</p>
		<p>You can add new reply to the thread using the form at top of the thread's detail page or by using quick reply button.</p>
		<p><b>Quick Reply</b> is the easy way to reply to a post. However, quick reply may not be usable on mobile devices as it requires Javascript. Quick Reply posts your reply without reloading the page.</p>
	</div>



	<!-- Imageboard -->
	<div class="card-footer faq" id="imageboard">
		<h3>Imageboard</h3>
		<p>Imageboard is a website where discussion are created with images and most of the reply are done using images (image macros to be exact) or using text. NPChan is a discussion board but isn't a pure imageboard as it doesn't require an image to start a new thread. Some popular imageboard existing currently are 4chan and 8chan and NPChan tries to be as much close as them in functionality while retaining its own uniqueness.</p>
	</div>



	<!-- Anonymous -->
	<div class="card-footer faq" id="anonymous">
		<h3>Anonymous</h3>
		<p>Anonymous (anon, in short) means someone whose identity is not known. NPChan is an anonymous discussion board which implies, NPChan doesn't reveals your identity to anyone nor asks any of your personal information. Anonymous is the default name of every user in NPChan.</p>
	</div>



	<!-- Account -->
	<div class="card-footer faq" id="account">
		<h3>Account (Login/Registration)</h3>
		<p>NPChan doesnot require an account for any purpose. This means, you do not have to create a new account to begin using NPChan. Just use the form on top of the page to create new thread or reply to one without providing any of your information.</p>
	</div>



	<!-- Preview -->
	<div class="card-footer faq" id="preview">
		<h3>Image Preview</h3>
		<p>By default, NPChan shows a image posted in thread and reply in thumbnail (small size). To view the full image, you can click the image and it will enlarge the image on same position.</p>
		<p>Video Preview works with same mechanism but while closing the video, you have to click on <b>- Close Video</b> button.</p>
		<p>If your browser doesn't support javascript (on mobile or on old version of browser), you will be redirected to full image source.</p>
		<p>To minimize the image back to thumbnail size, click on that large image again.</p>
	</div>



	<!-- Locked Thread -->
