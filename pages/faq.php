	<!-- Cite -->
	<div class="card-footer faq" id="cite">
		<h3>Cite (Reply to Reply)</h3>
		<p>If you wish to reply to a reply, use <code>&gt;&gt;POST_NO</code> on your post/reply.</p>
		<p>You can cite multiple replies. Just add the code and you are good to go.</p>
		<p><b>The post number is a unique number displayed at each thread/reply (<b>No. 12345</b>, where 12345 is the post number).</b></p>
		<p>Example: <code>&gt;&gt;12345</code> will cite to the thread or reply of number 12345.</p>
		<p>You can cite any thread or reply from any board in your post.</p>
		<p>Putting your mouse on top of the citation code will show you a preview of that thread/reply. Clicking on that number will take you to the thread.</p>
	</div>


