	</div>



	<!-- Thread Archived -->
	<div class="card-footer faq" id="archive">
		<h3>Thread Archived</h3>
		<p>A thread is archived when it is no longer active or has met its limit.</p>
		<p>Posts with few replies (less than 10) will not be archived.</p>
		<p>A thread gets archived if there is no activity for 12 hours on that thread.</p>
		<p>A thread if it reaches 250 replies will get archived. This is the maximum number of replies one thread can get.</p>
		<p>Low engagement thread gets <a href="#deleted">Deleted</a></p>
		<p>While a thread gets archived, all the information of user (IP Address, Browser Info) gets removed.</p>
		<p>Archive process is done automatically. <a href="#moderator">Moderators</a> cannot archive a page.</p>
	</div>



	<!-- Thread Deleted -->
	<div class="card-footer faq" id="deleted">
		<h3>Thread Deleted</h3>
		<p>When a thead is no longer active for more than 12 hours (no replies) and has less than 10 replies, it will get deleted. If it has more than 10 replies, it will be <a href="#archive">Archived</a>.</p>
		<p>When a post gets deleted, we permanently delete all the contents within (including: replies, images, user information associated with thread).</p>
		<p>If you start a thread, you can <a href="#delete">delete</a> that thread permanently.</p>
		<p>If a thread is archived, you cannot delete it by yourself.</p>
	</div>



	<!-- Thread Bump -->
	<div class="card-footer faq" id="bump">
		<h3>Thread "Bump"</h3>
		<p>Bump or "Bumping" is the process of boosting a thread. Every reply bumps a thread a little bit.</p>
		<p>Recently bumped thread appears first on the boards page and overboard.</p>
		<p>A thread can only be bumped once every minute (except for <b>random</b>(/b/) board, 30 seconds). Which means, even if you reply inbetween that interval, that thread will not be bumped.</p>
		<p><b>If you do not like the thread, do not reply to it as it will get bumped.</b></p>
	</div>



	<!-- Sage -->
	<div class="card-footer faq" id="sage">
		<h3>Sage</h3>
		<p>Sage is opposite of bump. A sage decreases the last bump time for one minute (30 sec for <b>random</b> (/b/) board) which causes the thread to get pushed back from top of the board.</p>
		<p><b>Only sage a thread only if you feel that thread shouldn't be on the board or is inappropriate. "Saging" a thread just because you don't agree to that person's opinion is against our rules, which may result in <a href="#ban">ban</a>.</b></p>
		<p>You can sage a thread by replying <b>sage</b> or <b>saged</b> on reply. Do not add any other text there else we can't determine if you saged a post or not (but you can upload an image on that sage reply).</p>
	</div>




